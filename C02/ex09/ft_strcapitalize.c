char *ft_strcapitalize(char *str)
{
	int i;
	
	i = 0;
	while (str[i] != '\0')
	{
		if('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		if ((str[i] == 32) || (str[i] == 45) || (str[i] == 43))
		{
			if ('a' <= str[i + 1] && str[i + 1] <= 'z')
				str[i + 1] -= 32;
		}
		i++;
	}
	str[0] -= 32;
	return (str);
}
