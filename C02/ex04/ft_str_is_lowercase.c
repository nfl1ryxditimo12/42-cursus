int	ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (('a' <= str[i] && str[i] <= 'z') == 0)
			return (0);
	}
	return (1);
}
