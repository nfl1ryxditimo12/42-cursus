#include <unistd.h>

int	main(void)
{
	char a[7];

	a[0] = '0' - 1;
	a[2] = ' ';
	a[5] = ',';
	a[6] = ' ';
	while (++a[0] <= '9')
	{
		a[1] = a[0] - 1;
		while (a[1]++ <= '9')
		{
			a[3] = a[0] - 1;
			while (++a[3] <= '9')
			{
				a[4] = a[1];
				while (++a[4] <= '9')
				{
					write(1, a, 7);
				}
			}
		}
	}
	return (0);
}
