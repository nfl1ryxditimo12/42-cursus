#include <unistd.h>

void	repeat(char *a, int i, int j)
{
	write(1, a, 7);
	if (a[j] == '9')
	{
		++a[i];
		a[j] = '0';
	}
	else
		++a[j];
}

void	ft_print_comb2(void)
{
	char	a[7];

	a[0] = '0';
	a[1] = '0';
	a[2] = ' ';
	a[5] = ',';
	a[6] = ' ';
	while (a[0] != '9' || a[1] != '8')
	{
		a[3] = a[0];
		if (a[1] == '9')
		{
			++a[3];
			a[4] = '0';
		}
		else
			a[4] = a[1] + 1;
		while (a[3] != '9' || a[4] != '9')
			repeat(a, 3, 4);
		repeat(a, 0, 1);
	}
	write(1, "98 99", 5);
}
