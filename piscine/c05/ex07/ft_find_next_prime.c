int	ft_is_prime(int nb)
{
	long long	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	if (ft_is_prime(nb) == 1)
		return (nb);
	if (++nb % 2 == 0)
		nb++;
	while (1)
	{
		if (ft_is_prime(nb) == 1)
			return (nb);
		nb = nb + 1;
	}
}
